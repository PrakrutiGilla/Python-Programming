import numpy as np
 
array1 = np.array([[10, 20, 30], [40, 50, 60]])
 
print("Mean: ", np.mean(array1))
 
print("Std: ", np.std(array1))
 
print("Var: ", np.var(array1))
 
print("Sum: ", np.sum(array1))
 
print("Prod: ", np.prod(array1))

print("cumulative sum: ",np.cumsum(array1))

print("cumulative product: ",np.cumprod(array1))

print("max value: ",np.max(array1))

print("max value index: ",np.argmax(array1))

print("min value: ",np.min(array1))

print("min value index: ",np.argmin(array1))




 
